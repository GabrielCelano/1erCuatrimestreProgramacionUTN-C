/** \brief
 *  Valida un char ingresado
 *  \param Recibe un char a validar
 *  \return Retorna el char validado
 */
char pedirOpcion(char opcion);

/**
 * Ingreso el operador A o B
 * @param numIngresado Recibe un numero de tipo float tanto para A o para B
 * @return Retorna el numero tipo float tanto para A o para B
 */
double pedirNumero(double numIngresado);

/**
 * Realiza la suma de los dos numeros ingresados
 * @param numIngresado1 Recibe el numero A de tipo float
 * @param numIngresado2 Recibe el numero B de tipo float
 * @return Retorna la suma de los numeros
 */
double suma(double numIngresado1, double numIngresado2);

/**
 * Realiza la resta de los dos numeros ingresados
 * @param numIngresado1 Recibe el numero A de tipo float
 * @param numIngresado2 Recibe el numero B de tipo float
 * @return Retorna la resta de los numeros
 */
double rest(double numIngresado1, double numIngresado2);

/**
 * Realiza la division de los dos numeros ingresados
 * @param numIngresado1 Recibe el numero A de tipo float
 * @param numIngresado2 Recibe el numero B de tipo float
 * @return Retorna la division de los numeros
 */
double divi(double numIngresado1, double numIngresado2);

/**
 * Realiza la multiplicacion de los dos numeros ingresados
 * @param numIngresado1 Recibe el numero A de tipo float
 * @param numIngresado2 Recibe el numero B de tipo float
 * @return Retorna la multiplicacion de los numeros
 */
double mult(double numIngresado1, double numIngresado2);

/**
 * Realiza el factorial de un numero
 * @param numIngresado1 Recibe el numero A de tipo float
 */
double fact1(double numIngresado1);

/**
 * Realiza el factorial de un numero
 * @param numIngresado2 Recibe el numero B de tipo float
 * @return Retorna el factorial del numero
 */
double fact2(double numIngresado2);


